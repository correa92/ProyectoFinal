

export class Pelicula {
    constructor(
      id,
      nombrePelicula,
      formato,
      genero,
      estreno,
      idioma,
      sala,
      precio,
      imagen
    ) {
      this.id = id;
      this.nombrePelicula = nombrePelicula;
      this.formato = formato;
      this.genero = genero;
      this.estreno = estreno;
      this.idioma = idioma;
      this.sala = sala;
      this.precio = precio;
      this.imagen = imagen;
    }
}
    

    